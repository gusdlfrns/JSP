package j0925;

public class incTest {

	public static void main(String[] args) {
		int n = 10;
		System.out.println("++n : "+(++n));
		n=10;
		System.out.println("n++ : "+(n++));
		System.out.println("n : "+n);
		
		n=10;
		System.out.println("--n : "+(--n));
		n=10;
		System.out.println("n-- : "+(n--));
		System.out.println("n : "+n);
	}

}
